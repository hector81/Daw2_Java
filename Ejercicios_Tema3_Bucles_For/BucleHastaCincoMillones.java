package Ejercicios_Tema3_Bucles_For;

public class BucleHastaCincoMillones {
//main
    public static void main(String[] args) {
        int i;
        
        for(i =5; i <= 5000000 ; i *=10){
            System.out.print("" + i + " ");
        }
        System.out.print("");
    }//fin main
}//fin clase
