    private void jButtonBajaAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBajaAceptarActionPerformed
        try {
            //creamos objeto con sus parametros
            String matricula = jTextFieldMatriculaAlta.getText();
            Veleros velero = new Veleros(matricula);
            if (cursor.darBajaVeleros(velero) != false) {
                JOptionPane.showMessageDialog(this, "Velero borrado");
            } else {
                JOptionPane.showMessageDialog(this, "Velero no puede ser borrado");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "En estos momentos no se puede realizar la operacion");
        }
    }//GEN-LAST:event_jButtonBajaAceptarActionPerformed
