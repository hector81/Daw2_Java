    private void jButtonModicarAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModicarAceptarActionPerformed
            String matricula = jTextFieldDniModificar.getText();
            Barco viejo= new Barco(matricula);
            if(viejo==null){
                JOptionPane.showMessageDialog(this, "No existe ese barco.");
            }else{
                //datos nuevos pdiendolos por la ventana
                jLabelMatriculaAlta.setText("Nueva Matricula:");
                jLabelEsloraAlta.setText("Nueva Eslora:");
                jLabelYearFabricacionAlta.setText("Nuevo aÃ±o de fabricaciÃ³n:");
                jLabelPrecioAlta.setText("Nuevo Precio:");
                jLabelNombreAlta.setText("Nuevo Nombre:");
                jLabelMangaAlta.setText("Nuevo Manga:");
                //escribimos los datos
                String matriculaNueva = jTextFieldMatriculaAlta.getText();
                int esloraMetrosNueva = Integer.parseInt(jTextFieldEsloraAlta.getText());
                int yearFabricacionNueva = Integer.parseInt(jTextFieldYearFabricacionAlta.getText());
                double precioNueva = Double.parseDouble(jTextFieldPrecioAlta.getText());
                String nombreNueva = jTextFieldNombreAlta.getText();
                int mangaMetrosNueva = Integer.parseInt(jTextFieldMangaAlta.getText());

                //creamos el objeto nuevo
                Barco nuevo = new Barco(matriculaNueva,esloraMetrosNueva ,yearFabricacionNueva,
                precioNueva,nombreNueva,mangaMetrosNueva );
                //llamamos al metodo modificar
                if(cursor.modificarBarco(viejo, nuevo)==true){
                    cursor.modificarBarco(viejo, nuevo);
                    JOptionPane.showMessageDialog(this,"Barco modificado.");
                }
            }
    }//GEN-LAST:event_jButtonModicarAceptarActionPerformed
