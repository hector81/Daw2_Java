    private void jButtonAltaAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAltaAceptarActionPerformed
        try{
        //creamos un barco auxiliar con los datos pasados en JTEXTFIELD
        esRequerido(jTextFieldMatriculaAlta.getText(), " matricula");
            String matricula = jTextFieldMatriculaAlta.getText();
            int esloraMetros = Integer.parseInt(jTextFieldEsloraAlta.getText());
            int yearFabricacion = Integer.parseInt(jTextFieldYearFabricacionAlta.getText());
            double precio = Double.parseDouble(jTextFieldPrecioAlta.getText());
            String nombre = jTextFieldNombreAlta.getText();
            int mangaMetros = Integer.parseInt(jTextFieldMangaAlta.getText());
            Barco auxBarco = new Barco(matricula, esloraMetros, yearFabricacion, precio, nombre, mangaMetros);
          
            if (cursor.darAltaBarco(auxBarco)) {
                JOptionPane.showMessageDialog(this, "Barco aÃ±adido con exito", "OK", 1);
            } else {
                JOptionPane.showMessageDialog(this, "Ya existe el barco con esta matricula", "Error", 2);
            }
        }catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(this,"Los campos matricula y eslora deben ser numeros enteros");
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this,"En estos momentos no se puede realizar la operacion");
        }
    }//GEN-LAST:event_jButtonAltaAceptarActionPerformed
