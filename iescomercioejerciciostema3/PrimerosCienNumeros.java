package iescomercioejerciciostema3;

public class PrimerosCienNumeros {
    //main
    public static void main(String[] args) {
        //declarar variable
        int contador = 0;

        
        //bucle while
        while(contador <= 100){
            contador++;
            System.out.println(contador);
        }
        
        
    }//fin main
}//fin clase

